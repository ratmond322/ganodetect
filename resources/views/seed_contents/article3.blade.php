<p>Manajemen lapangan yang baik dapat menekan laju penyebaran Ganoderma. Beberapa langkah operasional yang efektif:</p>
<ul>
  <li><strong>Sanitasi:</strong> singkirkan sisa tanaman yang terinfeksi dan jangan biarkan material menular berserakan.</li>
  <li><strong>Perbaikan drainase:</strong> mengurangi kelembaban tinggi di area perakaran yang memicu pertumbuhan jamur.</li>
  <li><strong>Pemantauan berkala:</strong> jadwalkan inspeksi lapangan dan rekam data untuk pembuatan strategi.</li>
</ul>
<p>Pelatihan untuk petani lokal juga krusial agar praktik pencegahan diterapkan konsisten di lapangan.</p>
